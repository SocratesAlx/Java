    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String staffID = jTextField1.getText();

    if (staffID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter the Staff ID to update the record.");
        return;
    }

    // Retrieve data from other text fields
    String firstName = jTextField2.getText();
    String lastName = jTextField3.getText();
    String fatherName = jTextField4.getText();
    String motherName = jTextField5.getText();
    String taxNumber = jTextField6.getText();
    String insuranceNumber = jTextField7.getText();
    String homeAddress = jTextField8.getText();
    String primaryEmail = jTextField9.getText();
    String secondaryEmail = jTextField10.getText();
    String maritalStatus = jTextField11.getText();
    String children = jTextField12.getText();
    String education = jTextField13.getText();
    String startDate = jTextField14.getText();
    String endDate = jTextField15.getText();

    // SQL query to update the record where StaffID matches
    String query = "UPDATE Staff SET FirstName = ?, LastName = ?, FatherName = ?, MotherName = ?, TaxNumber = ?, "
                 + "InsuranceNumber = ?, HomeAddress = ?, PrimaryEmail = ?, SecondaryEmail = ?, MaritalStatus = ?, "
                 + "DependentChildren = ?, Education = ?, StartDate = ?, EndDate = ? WHERE StaffID = ?";

    try (Connection con = DBConnection.connect();
         PreparedStatement pst = con.prepareStatement(query)) {

        // Set parameters for the query
        pst.setString(1, firstName);
        pst.setString(2, lastName);
        pst.setString(3, fatherName);
        pst.setString(4, motherName);
        pst.setString(5, taxNumber);
        pst.setString(6, insuranceNumber);
        pst.setString(7, homeAddress);
        pst.setString(8, primaryEmail);
        pst.setString(9, secondaryEmail);
        pst.setString(10, maritalStatus);
        
        if (children.isEmpty()) {
            pst.setNull(11, java.sql.Types.INTEGER);
        } else {
            pst.setInt(11, Integer.parseInt(children));
        }
        
        pst.setString(12, education);
        pst.setString(13, startDate);

        // Handle EndDate: Set NULL if empty
        if (endDate.isEmpty()) {
            pst.setNull(14, java.sql.Types.DATE);
        } else {
            pst.setString(14, endDate);
        }

        pst.setInt(15, Integer.parseInt(staffID)); // StaffID as WHERE condition

        // Execute update
        int rowsUpdated = pst.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Record updated successfully!");
        } else {
            JOptionPane.showMessageDialog(this, "No matching Staff ID found.");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values where required: " + e.getMessage());
    }

    }//GEN-LAST:event_jButton3ActionPerformed

