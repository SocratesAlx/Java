    public static java.sql.Date parseDate(String dateStr) {
    if (dateStr == null || dateStr.trim().isEmpty()) {
        return null;
    }

    String[] formats = {"yyyy-MM-dd", "MM/dd/yyyy", "dd-MM-yyyy", "yyyy/MM/dd"};
    for (String format : formats) {
        try {
            return new java.sql.Date(new SimpleDateFormat(format).parse(dateStr).getTime());
        } catch (ParseException e) {
            // Try next format
        }
    }
    JOptionPane.showMessageDialog(null, "Invalid date format: " + dateStr);
    return null;
}
    
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String firstName = jTextField2.getText();
    String lastName = jTextField3.getText();
    String fatherName = jTextField4.getText();
    String motherName = jTextField5.getText();
    String taxNumber = jTextField6.getText();
    String insuranceNumber = jTextField7.getText();
    String homeAddress = jTextField8.getText();
    String primaryEmail = jTextField9.getText();
    String secondaryEmail = jTextField10.getText();
    String maritalStatus = jTextField11.getText();
    String children = jTextField12.getText();
    String education = jTextField13.getText();
    String startDate = jTextField14.getText();
    String endDate = jTextField15.getText();

    // SQL Query for inserting into the Staff table
    String query = "INSERT INTO Staff (FirstName, LastName, FatherName, MotherName, TaxNumber, InsuranceNumber, "
                 + "HomeAddress, PrimaryEmail, SecondaryEmail, MaritalStatus, DependentChildren, Education, StartDate, EndDate) "
                 + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";

    try (Connection con = DBConnection.connect(); // Get connection from DBConnection
         PreparedStatement pst = con.prepareStatement(query)) {
        
        // Set parameters for the query
        pst.setString(1, firstName);
        pst.setString(2, lastName);
        pst.setString(3, fatherName);
        pst.setString(4, motherName);
        pst.setString(5, taxNumber);
        pst.setString(6, insuranceNumber);
        pst.setString(7, homeAddress);
        pst.setString(8, primaryEmail);
        pst.setString(9, secondaryEmail);
        pst.setString(10, maritalStatus);
        pst.setInt(11, Integer.parseInt(children));
        pst.setString(12, education);
        pst.setString(13, startDate);

        // Handle EndDate: Set NULL if empty
        if (endDate.isEmpty()) {
            pst.setNull(14, java.sql.Types.DATE);
        } else {
            pst.setString(14, endDate);
        }

        // Execute the insert operation
        int rowsInserted = pst.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Staff record inserted successfully!");
            
            // Clear all text fields after successful insertion
            clearTextFields();
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid number for Dependent Children: " + e.getMessage());
    }
} 

