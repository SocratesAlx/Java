// Displaying multiple lines with method System.out.printf.


public class Welcome4 
{
    // main method begins execution of Java application
    public static void main(String[] args)
    {
        System.out.printf("%s%n%s%n",   // the %s is a placeholden for a string, and the %n specifier to jump lines instead of /n,because 5n is portable between all operating systems.
        "Welcome to", "Java Programming!");
    } // end method main
} //  end class Welcome4
